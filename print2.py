"""
str = string
int = inteiro
"""
print("Olá, mundo!")  # string com aspas duplas.
print('Olá, mundo!')  # string com aspas simples.
print(123456)  # inteiro, não precisa de aspas.
print("Isso é uma 'string' (str)!")  # aspas simples dentro da aspas duplas.
print('Isso é uma "string" (str)!')  # aspas duplas dentro da aspas simples.
print("Isso é uma \"string\" (str)!")  # formatação de aspas duplas dentro da aspas duplas (\ \).
print('Isso é uma \'string\' (str)!')  # formatação de aspas simples dentro da aspas simples (\ \).